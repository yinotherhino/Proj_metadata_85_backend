$("#docs-intro").click(function(){
    $(".main-docs").show();
    $(".getting_started").hide();
    $(".libraries").hide();
});

$("#docs-started").click(function(){
    $(".main-docs").hide();
    $(".getting_started").show();
    $(".libraries").hide();
});


$("#docs-libraries").click(function(){
    $(".main-docs").hide();
    $(".getting_started").hide();
    $(".libraries").show();
});


$(".js-btn").click(function(){
    $(".js-btn-x").show
    $(".js-btn").hide();
    $(".links h5").show();
    $(".links h6").show()
    

});

$(".js-btn-x").click(function(){
    $(".js-btn-x").hide
    $(".js-btn").show();
    $(".links h5").hide();
    $(".links h6").hide()
    

});
